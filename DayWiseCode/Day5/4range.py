# range() function in python

# range() function in python
# range() function is used to generate a sequence of numbers. It is used in for loops to iterate through a sequence of numbers.

# Syntax:
# range(start, stop, step)

# eg:
# for i in range(1, 10, 2):
#     print(i)
# Output:
# 1
# 3
# 5
# 7
# 9

print("@ range()")

for number in range(10):
    print(number)

for n in range(20,25):
    print(n)

# printing even or odd number using skip
for skip in range(0,10,2):
    print(skip)
    

