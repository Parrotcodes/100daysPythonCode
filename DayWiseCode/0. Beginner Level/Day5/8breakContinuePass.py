# Break, Continue and Pass

# Break
for i in range(0,10):
    if i==5:
        break
    print(i)


# Continue
for i in range(0,10):
    if i==5:
        continue
    print(i)


# Pass  -- future use
for i in range(0,10):
    if i==5:
        pass
    print(i)


